fun main(){
    val numbers = arrayOf(1, 2, 3)
    
    val updatedNumbers = numbers.filter { it != 3 }.toTypedArray()
    println(numbers.joinToString())
    println(updateNumbers.joinToString()) 

    val string = arrayOf("A", "B", "C", "D")
    val updatedString = string.filter { it != "C" }.toTypedArray()
    println(string.joinToString())
    println(updatedString.joinToString()
    )


}